                                <b class="d-inline-block mt-2 mb-2">Знаки альтерации</b>
                                <p>
                                    Знаки альтерации записываются рядом с нотой и указывают на повышение/понижение высоты звука на полтона. Полутона - это те самые ноты, которые играются на чёрных клавишах. Возможно, у вас возник вопрос, почему белых клавиш больше, чем чёрных. Ответим сразу – потому что некоторые ноты отличаются между собой только на полутон. Это ми и фа, а также си и до.
