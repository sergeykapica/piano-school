                        <h5>Критерии выбора пианино</h5>
                        <p>
                            Даже если вы не профессиональный настройщик, обратите внимание на некоторые детали при выборе пианино. Это поможет избежать неудачной покупки.
                        </p>
                        <h5>Состояния шпона</h5>
                        <p>
                            Если шпон набух или отслаивается, это признак того, что пианино неправильно хранилось, или в него попала влага. Разумеется, пианино может оказаться пригодным, но будет разумно в таком случае будет не рисковать и не приобретать инструмент с дефектом.
                        </p>
                        <h5>Клавиатура и педали</h5>
                        <p>
                            В идеале, чтобы проверить состояние клавиатуры, нужно снять верхний щит и вынуть клавиатурный клап. Однако, это требует присутствия настройщика. Тем не менее, можно оценить состояние клавиатуры по внешнему виду и звучанию. На клавишах не должно быть трещин, сколов и прочих видимых внешних дефектов. Поиграйте на пианино для оценки звука, сперва используя правую и левую педали поочередно, потом обе. Внимательно оцените звучание инструмента. Нажатие на педаль должно быть мягким, при этом звучание меняется: ослабевает и возрастает.
                        </p>
                        <h5>Дефекты, при которых не стоит покупать пианино</h5>
                        <p>
                            Вот перечень тех видимых дефектов, которые считаются верным признаком, что инструмент в нерабочем состоянии:

                            <ul class="w-100">
                                <li>глухие звуки;</li>
                                <li>посторонние звуки;</li>
                                <li>сколы, царапины и трещины на поверхности;</li>
                                <li>застревание или западание клавиш;</li>
                                <li>слишком свободное движение клавиш;</li>
                                <li>набухший или отслоившийся шпон.</li>
                            </ul>

                            Разумеется, для выбора пианино надежнее будет пригласить настройщика или мастера со стажем, чтобы наверняка избежать неудачной покупки, однако даже эти простые советы могут помочь избежать покупки неисправного инструмента.
