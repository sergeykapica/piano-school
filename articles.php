                                Кроме этого, знаки альтерации можно увидеть в начале пьесы, рядом с ключом. Они указывают тональность, в которой записано произведение и действуют до самого конца либо до смены тональности. Это означает, что ноты, на чьих линейках указаны знаки, нужно играть на полтона выше или ниже (причём во всех октавах) – если рядом с ними не указаны бекар (отменяет знак), дубль-диез (нота играется на тон выше указанной) или дубль-бемоль (на тон ниже указанной).
