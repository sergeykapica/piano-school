                        <p>
                            Выбор пианино – это нелегкое дело. Этот громоздкий инструмент не так просто обменять, перепродать или выбросить, поэтому стоит учитывать некоторые нюансы при выборе. Если нет возможности позвать настройщика или мастера для помощи в выборе, то есть общеизвестные вещи, на которые стоит обратить внимание.
                        </p>
                        <h5>Возраст пианино</h5>
                        <p>
                            Теоретически, "рабочими" считаются даже пианино конца 19-начала 20 века или пианино 50-70-ых годов. Однако эти модели считаются антиквариатом и требуют больших финансовых вложений.<br/><br/>

                            Зачастую выбор падает на пианино «Беларусь» 70-90-ых годов. Даже в состоянии б/у, такие инструменты подходят для обучения игре на пианино, хотя, конечно, многое зависит от состояния пианино. Также хорошим выбором считается пианино «Беларусь» 90-00-ых годов, но в этом случае было бы не лишним, чтобы с выбором помогал грамотный мастер, потому что не все инструменты этого периода хорошо звучат.
